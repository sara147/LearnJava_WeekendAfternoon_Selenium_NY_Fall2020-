public class LearnEquityAndRelationalOperator {


    // == Equal to
    // ! = not equal
    // > greater than
    //< less than
    // <= less than equal
    public static void main(String[] args) {

        int num1 = 50;

        if (num1 < 51) {

            // if condition is true then execute if block of code

            System.out.println(" number 1 value is less than to 50 ");
        }

        if (num1 != 51) {

            // if condition is true then execute if block of code

            System.out.println(" number 1 value is not equal to 50 ");

        }

        if (num1 > 51) {

            // if condition is true then execute if block of code

            System.out.println(" number 1 value is greater than 50 ");
        }
        if (num1 >= 51) {

            // if condition is true then execute if block of code

            System.out.println(" number 1 value is greater than equal to 50 ");

        }
        if (num1 <= 51) {

            // if condition is true then execute if block of code

            System.out.println(" number 1 value is less than equal to 50 ");
        }
    }
}