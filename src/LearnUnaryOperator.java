public class LearnUnaryOperator {



    public static void main(String[] args) {
        int number = 30;
        ++number; //1+30
        number++; //31 +    1 for the future
        number++;
        ++number;
        --number;
        --number;
        number++;
        number--;
        number--;
        number--;
        number--;
        --number;
        ++number;
        ++number;
        number++;
        number--;
        --number;


        System.out.println(number);


    }


}
